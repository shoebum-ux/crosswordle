// Validate if a word is in the word list
function isValidWord(word) {
    return WORD_LIST.includes(word.toUpperCase());
}

// Check guess and provide feedback
function checkGuess(verticalGuess, horizontalGuess) {
    const vWord = gameState.currentPair.vertical;
    const hWord = gameState.currentPair.horizontal;
    
    // Normalize inputs
    verticalGuess = verticalGuess.toUpperCase();
    horizontalGuess = horizontalGuess.toUpperCase();
    
    // Validate words
    if (!isValidWord(verticalGuess) || !isValidWord(horizontalGuess)) {
        return { valid: false, message: 'One or both words are not in the dictionary.' };
    }
    
    // Check if center letters match
    if (verticalGuess[2] !== horizontalGuess[2]) {
        return { valid: false, message: 'The center letters must match!' };
    }
    
    // Check if guess is correct
    const isCorrect = verticalGuess === vWord && horizontalGuess === hWord;
    
    // Generate feedback for each letter
    const feedback = {
        vertical: getLetterFeedback(verticalGuess, vWord),
        horizontal: getLetterFeedback(horizontalGuess, hWord)
    };
    
    return {
        valid: true,
        correct: isCorrect,
        feedback: feedback,
        verticalGuess: verticalGuess,
        horizontalGuess: horizontalGuess
    };
}

// Get feedback for each letter in a word
function getLetterFeedback(guess, target) {
    const feedback = [];
    const targetLetters = target.split('');
    const guessLetters = guess.split('');
    
    // Count occurrences of each letter in target
    const letterCounts = {};
    targetLetters.forEach(letter => {
        letterCounts[letter] = (letterCounts[letter] || 0) + 1;
    });
    
    // First pass: mark correct positions
    const result = new Array(5).fill('absent');
    guessLetters.forEach((letter, index) => {
        if (letter === targetLetters[index]) {
            result[index] = 'correct';
            letterCounts[letter]--;
        }
    });
    
    // Second pass: mark present letters
    guessLetters.forEach((letter, index) => {
        if (result[index] === 'absent' && letterCounts[letter] > 0) {
            result[index] = 'present';
            letterCounts[letter]--;
        }
    });
    
    return result;
}

// Update the visual board with feedback
function updateBoard(feedback, verticalGuess, horizontalGuess) {
    // Update vertical word with feedback classes
    const verticalCells = document.querySelectorAll('.vertical-word .letter-cell');
    verticalGuess.split('').forEach((letter, index) => {
        const cell = verticalCells[index];
        // Ensure the letter is displayed and add feedback color
        cell.textContent = letter;
        cell.classList.remove('correct', 'present', 'absent'); // Clear old feedback
        cell.classList.add(feedback.vertical[index]);
        cell.classList.add('animate');
        cell.classList.remove('selected'); // Remove selection after guess
    });
    
    // Update horizontal word with feedback classes (only visible cells)
    const horizontalCells = document.querySelectorAll('.horizontal-word .letter-cell');
    horizontalGuess.split('').forEach((letter, index) => {
        const cell = horizontalCells[index];
        // Only update the visible horizontal cells (not the transparent center)
        if (index !== 2) { // Skip center cell for horizontal word
            cell.textContent = letter;
            cell.classList.remove('correct', 'present', 'absent'); // Clear old feedback
            cell.classList.add(feedback.horizontal[index]);
            cell.classList.add('animate');
            cell.classList.remove('selected'); // Remove selection after guess
        }
    });
    
    // The center cell gets its feedback from the vertical word update above
}

// Add guess to history
function addToHistory(verticalGuess, horizontalGuess, result) {
    gameState.guessHistory.push({
        vertical: verticalGuess,
        horizontal: horizontalGuess,
        correct: result.correct,
        feedback: result.feedback
    });
    
    updateHistoryDisplay();
}

// Update history display
function updateHistoryDisplay() {
    const historySection = document.querySelector('.guess-history');
    const historyTitle = historySection.querySelector('h3');
    
    // Show/hide title based on whether there are guesses
    if (gameState.guessHistory.length > 0) {
        historyTitle.style.display = 'block';
    } else {
        historyTitle.style.display = 'none';
    }
    
    historyList.innerHTML = '';
    gameState.guessHistory.forEach((guess, index) => {
        const historyItem = document.createElement('div');
        historyItem.className = 'history-item';
        
        // Create vertical word with color-coded letters
        const verticalWordHTML = guess.vertical.split('').map((letter, i) => 
            `<div class="history-letter ${guess.feedback.vertical[i]}">${letter}</div>`
        ).join('');
        
        // Create horizontal word with color-coded letters
        const horizontalWordHTML = guess.horizontal.split('').map((letter, i) => 
            `<div class="history-letter ${guess.feedback.horizontal[i]}">${letter}</div>`
        ).join('');
        
        historyItem.innerHTML = `
            <div class="history-number">${index + 1}.</div>
            <div class="history-words">
                <div class="history-word">${verticalWordHTML}</div>
                <div class="history-word">${horizontalWordHTML}</div>
            </div>
        `;
        
        historyList.appendChild(historyItem);
    });
}

// Show game message
function showMessage(message, type = 'info') {
    gameMessage.textContent = message;
    gameMessage.className = `message ${type}`;
}

// Get words from grid
function getWordsFromGrid() {
    const verticalWord = gameState.gridState.v0 + gameState.gridState.v1 + gameState.gridState.center + gameState.gridState.v3 + gameState.gridState.v4;
    const horizontalWord = gameState.gridState.h0 + gameState.gridState.h1 + gameState.gridState.center + gameState.gridState.h3 + gameState.gridState.h4;
    return { verticalWord, horizontalWord };
}

// Handle game submission
function submitGuess() {
    if (gameState.gameOver) return;
    
    const { verticalWord, horizontalWord } = getWordsFromGrid();
    const verticalGuess = verticalWord.trim();
    const horizontalGuess = horizontalWord.trim();
    
    // Validate input length
    if (verticalGuess.length !== 5 || horizontalGuess.length !== 5) {
        showMessage('Both words must be exactly 5 letters long.', 'error');
        return;
    }
    
    // Check the guess
    const result = checkGuess(verticalGuess, horizontalGuess);
    
    if (!result.valid) {
        showMessage(result.message, 'error');
        return;
    }
    
    // Decrease attempts
    gameState.attempts--;
    updateAttemptsDisplay();
    
    // Update board with feedback
    updateBoard(result.feedback, result.verticalGuess, result.horizontalGuess);
    
    // Update keyboard colors
    updateKeyboardFeedback(result.feedback, result.verticalGuess, result.horizontalGuess);
    
    // Add to history
    addToHistory(result.verticalGuess, result.horizontalGuess, result);
    
    // Check if won
    if (result.correct) {
        gameState.won = true;
        gameState.gameOver = true;
        showMessage('Congratulations! You found both words!', 'success');
        return;
    }
    
    // Check if game over
    if (gameState.attempts === 0) {
        gameState.gameOver = true;
        showMessage(`Game over! The words were: ${gameState.currentPair.vertical} (vertical) and ${gameState.currentPair.horizontal} (horizontal)`, 'error');
        return;
    }
    
    // Don't clear the grid - keep letters with feedback colors
    // Just deselect any selected cell
    gridCells.forEach(c => c.classList.remove('selected'));
    gameState.selectedCell = null;
    gameState.previousCell = null;
    
    showMessage(`${gameState.attempts} attempts remaining.`, 'info');
}

// Update keyboard colors based on feedback
function updateKeyboardFeedback(feedback, verticalGuess, horizontalGuess) {
    // Update colors for vertical word
    for (let i = 0; i < verticalGuess.length; i++) {
        const letter = verticalGuess[i];
        const status = feedback.vertical[i];
        updateKeyColor(letter, status);
    }
    
    // Update colors for horizontal word
    for (let i = 0; i < horizontalGuess.length; i++) {
        const letter = horizontalGuess[i];
        const status = feedback.horizontal[i];
        updateKeyColor(letter, status);
    }
}

