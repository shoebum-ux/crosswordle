// Validate word pairs and create a clean list
const VALID_PAIRS = WORD_PAIRS.filter(pair => {
    const vWord = pair.vertical;
    const hWord = pair.horizontal;
    
    // Check if both words are in the word list
    if (!WORD_LIST.includes(vWord) || !WORD_LIST.includes(hWord)) {
        return false;
    }
    
    // Check if they actually intersect at the center (3rd letter, index 2)
    if (vWord[2] !== hWord[2]) {
        return false;
    }
    
    return true;
});

