// Physical keyboard support
document.addEventListener('keydown', function(event) {
    if (gameState.gameOver) return;
    
    const key = event.key.toUpperCase();
    
    // Handle special keys
    if (key === 'ENTER') {
        event.preventDefault();
        submitGuess();
    } else if (key === 'BACKSPACE') {
        event.preventDefault();
        handleBackspace();
    } else if (key === 'ESCAPE') {
        event.preventDefault();
        clearWords();
    } else if (key === 'TAB') {
        event.preventDefault();
        handleTabNavigation(event.shiftKey);
    } else if (key.startsWith('ARROW')) {
        event.preventDefault();
        handleArrowNavigation(key);
    } else if (key.length === 1 && key >= 'A' && key <= 'Z') {
        // Only allow single alphabet characters
        event.preventDefault();
        handleKeyInput(key);
    }
    // Ignore all other keys (META, CONTROL, ALT, etc.)
});

// Auto-select first cell on new game
function autoSelectFirstCell() {
    const firstCell = document.querySelector('[data-pos="v0"]');
    if (firstCell) {
        selectCell(firstCell);
    }
}

// Handle Tab navigation
function handleTabNavigation(isShiftPressed) {
    if (!gameState.selectedCell) {
        autoSelectFirstCell();
        return;
    }
    
    // Define the complete navigation sequence
    const navigationSequence = ['v0', 'v1', 'center', 'v3', 'v4', 'h0', 'h1', 'h3', 'h4'];
    const currentIndex = navigationSequence.indexOf(gameState.selectedCell);
    
    if (currentIndex === -1) return;
    
    let nextIndex;
    if (isShiftPressed) {
        // Shift+Tab: move backward
        nextIndex = currentIndex > 0 ? currentIndex - 1 : navigationSequence.length - 1;
    } else {
        // Tab: move forward
        nextIndex = currentIndex < navigationSequence.length - 1 ? currentIndex + 1 : 0;
    }
    
    const nextPos = navigationSequence[nextIndex];
    const nextCell = findCellByPosition(nextPos);
    if (nextCell) {
        selectCell(nextCell);
    }
}

// Handle Arrow key navigation
function handleArrowNavigation(key) {
    if (!gameState.selectedCell) {
        autoSelectFirstCell();
        return;
    }
    
    const verticalPositions = ['v0', 'v1', 'center', 'v3', 'v4'];
    const horizontalPositions = ['h0', 'h1', 'center', 'h3', 'h4'];
    
    let nextPos = null;
    
    switch (key) {
        case 'ARROWUP':
            // Move up in vertical word
            if (verticalPositions.includes(gameState.selectedCell)) {
                const currentIndex = verticalPositions.indexOf(gameState.selectedCell);
                if (currentIndex > 0) {
                    nextPos = verticalPositions[currentIndex - 1];
                }
            } else {
                // If in horizontal word, move to center
                nextPos = 'center';
            }
            break;
            
        case 'ARROWDOWN':
            // Move down in vertical word
            if (verticalPositions.includes(gameState.selectedCell)) {
                const currentIndex = verticalPositions.indexOf(gameState.selectedCell);
                if (currentIndex < verticalPositions.length - 1) {
                    nextPos = verticalPositions[currentIndex + 1];
                }
            } else {
                // If in horizontal word, move to center
                nextPos = 'center';
            }
            break;
            
        case 'ARROWLEFT':
            // Move left in horizontal word
            if (horizontalPositions.includes(gameState.selectedCell)) {
                const currentIndex = horizontalPositions.indexOf(gameState.selectedCell);
                if (currentIndex > 0) {
                    nextPos = horizontalPositions[currentIndex - 1];
                }
            } else {
                // If in vertical word, move to center
                nextPos = 'center';
            }
            break;
            
        case 'ARROWRIGHT':
            // Move right in horizontal word
            if (horizontalPositions.includes(gameState.selectedCell)) {
                const currentIndex = horizontalPositions.indexOf(gameState.selectedCell);
                if (currentIndex < horizontalPositions.length - 1) {
                    nextPos = horizontalPositions[currentIndex + 1];
                }
            } else {
                // If in vertical word, move to center
                nextPos = 'center';
            }
            break;
    }
    
    if (nextPos) {
        const nextCell = findCellByPosition(nextPos);
        if (nextCell) {
            selectCell(nextCell);
        }
    }
}

